    private void checkPermissions() {
    	String name = player.getStatus();
    	if (name.equals("Nomad")) {
    		permitGoverning(false);
    		permitPropose(true);
    		permitCoup(false);
    		permitTravel(true);
    	}else if (name.equals("Citizen")) {
    		permitGoverning(false);
    		permitPropose(false);
    		permitNationName(true);
    		permitTravel(true);
    		//UPDATE TO DISALLOW COUP IF IN DEMO, ALLOW COUP IF IN DICT *******
    	}else if (name.equals("President")) {
    		permitTravel(true);
    		permitGoverning(true);
    		permitPropose(false);
    		permitCoup(false);
    		permitNationName(true);
    	}else if (name.equals("Dictator")) {
    		permitTravel(true);
    		permitGoverning(true);
    		permitPropose(false);
    		permitCoup(false);
    		permitNationName(true);
    	}
    }
    
    private void showTerrainInfo() {
    	Territory ground = player.getGround();
    	curTerField.setText(ground.getName());
    	if (ground.getNation != null) {
    		curNationField.setText(ground.getNation());
    	}else {
    		curNationField.setText("none");
    	}
    	curTerrainField.setText(ground.getTerrain().toString());
    	curResourceField.setText(ground.getResource().toString());
    }
    
