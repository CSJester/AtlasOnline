    	WeGoalField.setText(""+player.getWeGoal());
    	PointsField.setText(""+player.getPoints());
    	goalsTotalField.setText(""+(player.getKnGoal()+player.getEnGoal()+player.getPoGoal()+player.getPrGoal()+player.getWeGoal()));
